#输入
name = input('亲，输入你的名字：')
#输入和输出交叉运行
print("你的名字是：",name)
#输出
print('=======这是一条华丽的分割线=======')
#输出多个字符串。print会依次输出每个字符串，遇到逗号时就会出现一个空格。
print('hello','world','i','am','samu')    
#print也可以输出整数或计算结果
print(300)
print(100+200)
#还可以把计算结果输出的更漂亮美观，“'100+200'”是字符串而非数学公式。
print('100 + 200 = ',100+200)
