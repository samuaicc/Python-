#循环控制语句有break,continue,pass
#break（跳出整个循环）
for letter in "python":
    if letter == 'h':
        break       #当字母为h时结束整个循环,跳转到下一条语句
    print(letter)
print("===========fgx===========")
#continue(跳过当前循环)
for let in "python":
    if let == 'h':
        continue    #当字母为h时结束此次循环，开始下一次循环
    print(let)
print("===========fgx===========")
#pass(是一个空语句，是为了保持结构的完整性，pass不做任何事情，一般只用作占位符)
for lett in "python":
    if letter == 'h':
        pass
        #这是pass语句，是一个空语句，不做任何执行"
    print(lett)
    
    
